using Microsoft.AspNetCore.Mvc;
using PermissionApi.Attributes;
using PermissionApi.Models;

namespace PermissionApi.Controllers
{
    [ApiController]
    [Route("[controller]")]
    public class WeatherForecastController : ControllerBase
    {
        private static readonly string[] Summaries = new[]
        {
        "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
    };

        private readonly ILogger<WeatherForecastController> _logger;

        public WeatherForecastController(ILogger<WeatherForecastController> logger)
        {
            _logger = logger;
        }

        [HttpGet("Standart")]
        [AllowedFor(PermissionEnum.Get | PermissionEnum.Add)]
        public IEnumerable<WeatherForecast> Standart()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpGet("Premium")]
        [AllowedFor(PermissionEnum.Get | PermissionEnum.Add | PermissionEnum.Update)]
        public IEnumerable<WeatherForecast> Premium()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpGet("Ultra")]
        [AllowedFor(PermissionEnum.Get | PermissionEnum.Delete | PermissionEnum.Add
                    | PermissionEnum.Update)]
        public IEnumerable<WeatherForecast> Ultra()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpGet("Admin")]
        [AllowedFor(PermissionEnum.Get | PermissionEnum.Delete | PermissionEnum.Add
                    | PermissionEnum.Update | PermissionEnum.Special)]
        public IEnumerable<WeatherForecast> Admin()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }
    }
}